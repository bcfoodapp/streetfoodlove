func SetupTables(db *sqlx.DB) error {
	commands := [...]string{
		`
		CREATE TABLE IF NOT EXISTS User (
			ID CHAR(36) NOT NULL,
			Email VARCHAR(100) NULL,
			Username VARCHAR(100) UNIQUE NULL,
			FirstName VARCHAR(100) NULL,
			LastName VARCHAR(100) NULL,
			SignUpDate DATETIME NULL,
			LoginPassword BINARY(32) NULL,
			UserType TINYINT NULL,
			Photo CHAR(36),
			PRIMARY KEY (ID)
		)
		`,
		`
		CREATE TABLE IF NOT EXISTS Vendor (
			ID CHAR(36) NOT NULL,
			Name VARCHAR(100) NOT NULL,
			BusinessAddress VARCHAR(500) NULL,
			Website VARCHAR(500) NULL,
			BusinessHours VARCHAR(500) NOT NULL,
			Phone VARCHAR(50) NULL,
			BusinessLogo CHAR(36) NOT NULL,
			Latitude FLOAT NOT NULL,
			Longitude FLOAT NOT NULL,
			Owner CHAR(36) NOT NULL,
			PRIMARY KEY (ID),
			FOREIGN KEY (Owner) REFERENCES User(ID)
			ON DELETE CASCADE ON UPDATE CASCADE
		)
		`,
		`
		CREATE TABLE IF NOT EXISTS Reviews (
			ID CHAR(36) NOT NULL,
			Text text(500) NULL,
			VendorID CHAR(36) NOT NULL,
			UserID CHAR(36) NOT NULL,
			DatePosted DATETIME NULL,
			StarRating TINYINT NULL,
			PRIMARY KEY (ID),
			FOREIGN KEY (VendorID) REFERENCES Vendor(ID)
			ON DELETE CASCADE ON UPDATE CASCADE,
			FOREIGN KEY (UserID) REFERENCES User(ID)
			ON DELETE CASCADE ON UPDATE CASCADE
		)
		`,
		`
		CREATE TABLE Photos (
			ID CHAR(36) NOT NULL,
			DatePosted DATETIME NOT NULL,
			Text VARCHAR(500) NOT NULL,
			LinkID CHAR(36) NOT NULL,
			PRIMARY KEY (ID)
		)
		`,
		`
		CREATE TABLE Guide (
			ID CHAR(36) NOT NULL,
			Guide VARCHAR(5000) NOT NULL,
			DatePosted DATETIME NOT NULL,
			ArticleAuthor VARCHAR(500) NOT NULL,
			PRIMARY KEY (ID)
		)
		`,
		`
		CREATE TABLE Link (
			ID CHAR(36) NOT NULL,
			Title VARCHAR(45) NOT NULL,
			URL VARCHAR(255) NULL,
			PRIMARY KEY (ID)
		)
		`,
		`
		CREATE TABLE Favorite (
			ID CHAR(36) NOT NULL,
			DatePosted DATETIME NULL,
			VendorID CHAR(36),
			UserID CHAR(36),
			FOREIGN KEY (VendorID) REFERENCES Vendor(ID) ON DELETE CASCADE ON UPDATE CASCADE,
			FOREIGN KEY (UserID) REFERENCES User(ID) ON DELETE CASCADE ON UPDATE CASCADE,
			PRIMARY KEY (ID)
		)
		`,
	}

	for _, command := range commands {
		_, err := db.Exec(command)
		if err != nil {
			return err
		}
	}
	return nil
}

